package day25_ArrayList;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;

public class C03_CollectionsSort {

	public static void main(String[] args) {
		
		List<String> isimler=new ArrayList<>();
		
		isimler.add("Kutlu");
		isimler.add("Emrah");
		isimler.add("Ferhat");
		
		System.out.println(isimler);   //[Kutlu, Emrah, Ferhat]  olarak gelecek..
		
		Collections.sort(isimler);   //alfabetik sıraya ve buyukden kucuge göre dizecek şimdi
		
		System.out.println(isimler);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
