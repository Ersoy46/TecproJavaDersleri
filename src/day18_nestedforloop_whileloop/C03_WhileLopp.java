package day18_nestedforloop_whileloop;

public class C03_WhileLopp {

	public static void main(String[] args) {
		
		//20'den 30'a kadar olan sayilari (20 ve 30 dahil)
		
		for (int i = 20; i <=30; i++) {
			System.out.print(i+ " ");
		}
		System.out.println("");
		
		//while loop ile yazdirin
		
		int sayi=20;
		while (sayi<=30) {
			System.out.print(sayi + " ");
			sayi++;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
