package day19_whileloop_DoWhileLoop;

public class C04_DoWhileLoop {

	public static void main(String[] args) {

		
		int i =0;
		while (i<5) {
			System.out.print(i + " ");//0 1 2 3 4 yazdırır... 
			i++;
			
		}
		System.out.println("");
		
		//while loop önce kontrol eder sonra çalıştırır.
		//loop icinde artis yapilsa da bir sonraki kontrole kadar kod calismaya devam eder.
		//bu da bazı durumlarda hatali sonuclara ulasmamiza sebeb OLABİLİR....
		
		
		
		//DO-while Lopp ise önce islemi yapar sonra kontrol eder. 
		//özellikle kullnıcıdan deger almalar da Do-while Loop tercih edilir. 
		
		
		
	int a=0;
	
	do {
		System.out.print(a + " ");
		a++;
		
	} while (a<6);	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
