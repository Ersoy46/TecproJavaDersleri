package day17_forLoop;

public class C07_NestedForLoop {

	public static void main(String[] args) {
		
		// 1'den 4'e kadar sayılari yan yana 
		//aralarında 1 bosluk birakarak yazdrın....
		
		//NESTED FOR LOOP OLMASI İCİN ÖNEMLİ *****
		
	
		
		for (int satir = 1; satir <=4; satir++) {
			
		for (int i = 1; i <=4; i++) {
			
			System.out.print(satir*i + " ");
		}
		
		System.out.println("");
		
		
		
		
		
		
		
		
		
		}
	}
	}

