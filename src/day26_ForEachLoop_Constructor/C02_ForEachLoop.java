package day26_ForEachLoop_Constructor;

public class C02_ForEachLoop {

	public static void main(String[] args) {

		//bir integer array olustuunuz ve bu array'deki tum sayilarin
		//carpimin For-Each Loop kullanarak bulunuz ve sonucu ekrana yazdiriniz....
		
		int arr[]= {3,4,2,5};
	
		int carpim=1;
		
		for (int each: arr) {
			
			carpim*=each;
		}
		
		System.out.println("Array'lerin carpimi sonucu : " + carpim);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
